package audio;

public class AudioException extends Exception {

	public AudioException() {
		super();
	}

	public AudioException(String arg0, Throwable arg1) {
		super(arg0, arg1);
	}

	public AudioException(String arg0) {
		super(arg0);
	}

	public AudioException(Throwable arg0) {
		super(arg0);
	}
}
