package audio;

public class MusicException extends Exception {

	public MusicException() {
		super();
	}

	public MusicException(String arg0, Throwable arg1) {
		super(arg0, arg1);
	}

	public MusicException(String arg0) {
		super(arg0);
	}

	public MusicException(Throwable arg0) {
		super(arg0);
	}
}
