package logic_world.terrain;

public class WorldMapException extends Exception {

	public WorldMapException() {
		super();
	}

	public WorldMapException(String arg0, Throwable arg1) {
		super(arg0, arg1);
	}

	public WorldMapException(String arg0) {
		super(arg0);
	}

	public WorldMapException(Throwable arg0) {
		super(arg0);
	}
	
}
